package com.kj02;

public class ArrayTest01 {
	public static void main(String[] args) {
		//定义两个数组
		int[] arr =new int[2];
		int[] arr2 =new int[3];
		arr[1]=100;
		arr2[1]=20;
		//分别输出数组名和元素
		System.out.println("arr[1]"+arr[1]);
		System.out.println("arr2[0]"+arr2[0]);
		System.out.println("arr2[1]"+arr2[1]);
		System.out.println("arr2[2]"+arr2[2]);
	}

}
